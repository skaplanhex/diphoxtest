	IMPLICIT REAL*8 (A-H,L-Z)
	PARAMETER (K0MAX=84,J0MAX=21)
	DIMENSION RBORN(K0MAX),V1B(J0MAX),V2B(J0MAX)
	CALL VEC_BT(SC,TC,UC,N,V1B)
	CALL VEC_BT(SC,UC,TC,N,V2B)
	DO I=1,J0MAX
	  RBORN(I) = V1B(I)
	  RBORN(I+21) = V2B(I) 
	  RBORN(I+42) = V2B(I)
	  RBORN(I+63) = V1B(I)
	ENDDO 
	RETURN
	END	
